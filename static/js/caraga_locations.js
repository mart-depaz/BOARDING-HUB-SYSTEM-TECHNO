// Helper functions for location cascading
window.CaragaLocations = {
    data: caragaLocationData,

    // Get all regions (should return only Caraga)
    getRegions: function() {
        return Object.keys(caragaLocationData);
    },

    // Get provinces for a region
    getProvinces: function(region) {
        if (!region || !caragaLocationData[region]) return [];
        return Object.keys(caragaLocationData[region]);
    },

    // Get cities/municipalities for a province
    getCities: function(region, province) {
        if (!region || !province || !caragaLocationData[region] || !caragaLocationData[region][province]) {
            return [];
        }
        return Object.keys(caragaLocationData[region][province]);
    },

    // Get barangays for a city
    getBarangays: function(region, province, city) {
        if (!region || !province || !city ||
            !caragaLocationData[region] ||
            !caragaLocationData[region][province] ||
            !caragaLocationData[region][province][city]) {
            return [];
        }
        return caragaLocationData[region][province][city];
    },

    // Populate a select element with options
    populateSelect: function(selectElement, options, placeholder, currentValue) {
        if (!selectElement) return;

        selectElement.innerHTML = `<option value="">${placeholder}</option>`;

        options.forEach(option => {
            const optionElement = document.createElement('option');
            optionElement.value = option;
            optionElement.textContent = option;
            if (currentValue && option === currentValue) {
                optionElement.selected = true;
            }
            selectElement.appendChild(optionElement);
        });
    },

    // Initialize cascading dropdowns
    initCascading: function(config) {
        const {
            regionSelect,
            provinceSelect,
            citySelect,
            barangaySelect,
            currentRegion,
            currentProvince,
            currentCity,
            currentBarangay,
            onChange
        } = config;

        // Populate regions
        if (regionSelect) {
            this.populateSelect(
                regionSelect,
                this.getRegions(),
                'Select Region',
                currentRegion
            );

            regionSelect.addEventListener('change', () => {
                const region = regionSelect.value;

                // Clear and populate provinces
                if (provinceSelect) {
                    this.populateSelect(
                        provinceSelect,
                        this.getProvinces(region),
                        'Select Province'
                    );
                }

                // Clear dependent dropdowns
                if (citySelect) {
                    citySelect.innerHTML = '<option value="">Select City/Municipality</option>';
                }
                if (barangaySelect) {
                    barangaySelect.innerHTML = '<option value="">Select Barangay</option>';
                }

                if (onChange) onChange();
            });
        }

        // Province change handler
        if (provinceSelect) {
            provinceSelect.addEventListener('change', () => {
                const region = regionSelect ? regionSelect.value : currentRegion;
                const province = provinceSelect.value;

                // Populate cities
                if (citySelect) {
                    this.populateSelect(
                        citySelect,
                        this.getCities(region, province),
                        'Select City/Municipality'
                    );
                }

                // Clear barangays
                if (barangaySelect) {
                    barangaySelect.innerHTML = '<option value="">Select Barangay</option>';
                }

                if (onChange) onChange();
            });
        }

        // City change handler
        if (citySelect) {
            citySelect.addEventListener('change', () => {
                const region = regionSelect ? regionSelect.value : currentRegion;
                const province = provinceSelect ? provinceSelect.value : currentProvince;
                const city = citySelect.value;

                // Populate barangays
                if (barangaySelect) {
                    this.populateSelect(
                        barangaySelect,
                        this.getBarangays(region, province, city),
                        'Select Barangay'
                    );
                }

                if (onChange) onChange();
            });
        }

        // Barangay change handler
        if (barangaySelect) {
            barangaySelect.addEventListener('change', () => {
                if (onChange) onChange();
            });
        }

        // Initialize with current values
        if (currentRegion && regionSelect) {
            regionSelect.value = currentRegion;

            if (currentProvince && provinceSelect) {
                this.populateSelect(
                    provinceSelect,
                    this.getProvinces(currentRegion),
                    'Select Province',
                    currentProvince
                );

                if (currentCity && citySelect) {
                    this.populateSelect(
                        citySelect,
                        this.getCities(currentRegion, currentProvince),
                        'Select City/Municipality',
                        currentCity
                    );

                    if (currentBarangay && barangaySelect) {
                        this.populateSelect(
                            barangaySelect,
                            this.getBarangays(currentRegion, currentProvince, currentCity),
                            'Select Barangay',
                            currentBarangay
                        );
                    }
                }
            }
        }
    }
};

